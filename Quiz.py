# Quiz time

age = int(input("what is your age?: "))

days = int(age * 365)
decades = int(age / 10)
weeks = int(age * 52)
minutes = int(age * 525600)
print("You have been alive for", str(days) ," days ")
print("You have been alive for", str(decades) ,"decades")
print("You have been alive for", str(weeks) ,"weeks")
print("You have been alive for", str(minutes) ,"minutes")


